def resolve_year(dynasty, reign_name, reign_year, book_prefix=""):
    """Convert dynasty + reign + year to approximate CE year."""
    if book_prefix and book_prefix in BOOK_DYNASTY:
        dynasty = BOOK_DYNASTY[book_prefix]
    
    reign_map = DYNASTY_MAP.get(dynasty, {})
    if reign_name in reign_map:
        start, end = reign_map[reign_name]
        year = start + int(reign_year) - 1
        if year > end:
            year = end  # cap at end of reign
        return year
    return None
