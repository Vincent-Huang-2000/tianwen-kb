def resolve_year(dynasty, reign_name, reign_year):
    key = (dynasty, reign_name)
    if key in DYNASTY_STARTS:
        return DYNASTY_STARTS[key] + reign_year - 1
    return None

all_events = []

for fn in sorted(os.listdir('corpus')):
    if not fn.endswith('.txt') or '_tagged' in fn:
        continue
    prefix = fn.split('_')[0]
    if prefix not in BOOK_INFO:
        continue
    dynasty = BOOK_DYNASTY.get(prefix, '')
    book_title = BOOK_INFO[prefix]
    
    with open(os.path.join('corpus', fn), 'r', encoding='utf-8') as f:
        text = f.read()
    
    sentences = re.split(r'[。；\n]', text)
    for sent in sentences:
        sent = sent.strip()
        if len(sent) < 10:
            continue
        
        event_type = None
        for etype, keywords in EVENT_TYPES.items():
            for kw in keywords:
                if kw in sent:
                    event_type = etype
                    break
            if event_type:
                break
        if not event_type:
            continue
        
        # Skip theoretical discourse
        if re.search(r'(凡|占曰|经曰|所谓|一曰|皆|必|常|当|春秋.*日食)', sent):
            if not re.search(r'[一二三四五六七八九十百千\d]+年', sent):
                continue
        
        ce_year = None
        m = re.search(r'([\u4e00-\u9fff]{2,6})(元|[一二三四五六七八九十百千\d]+)年', sent)
        if m:
            reign = m.group(1)
            year_str = m.group(2)
            yr = 1 if year_str == '元' else cn2int(year_str)
            ce_year = resolve_year(dynasty, reign, yr)
        
        event = {'year': ce_year, 'type': event_type,
                 'description': sent.strip()[:200], 'source': book_title, 'prefix': prefix}
        all_events.append(event)

valid = [ev for ev in all_events if ev['year'] is not None]
valid.sort(key=lambda x: x['year'])

type_counts = {}
for ev in valid:
    type_counts[ev['type']] = type_counts.get(ev['type'], 0) + 1

print(f'Total: {len(all_events)}, with year: {len(valid)}')
if valid:
    print(f'Range: {valid[0]["year"]} to {valid[-1]["year"]}')
for t, c in sorted(type_counts.items(), key=lambda x: -x[1]):
    print(f'  {t}: {c}')

os.makedirs('data', exist_ok=True)
with open('data/events.json', 'w', encoding='utf-8') as f:
    json.dump(valid, f, ensure_ascii=False, indent=2)
print(f'Saved {len(valid)} events to data/events.json')

