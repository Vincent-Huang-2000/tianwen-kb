CN_NUM = {'一':1,'二':2,'三':3,'四':4,'五':5,'六':6,'七':7,'八':8,'九':9,'十':10}

def cn2int(s):
    s = s.strip()
    if s.isdigit(): return int(s)
    if '十' in s:
        parts = s.split('十')
        a = CN_NUM.get(parts[0], 1) if parts[0] else 1
        b = CN_NUM.get(parts[1], 0) if len(parts) > 1 and parts[1] else 0
        return a * 10 + b
    if '百' in s:
        parts = s.split('百')
        a = CN_NUM.get(parts[0], 1) if parts[0] else 1
        return a * 100
    return CN_NUM.get(s.strip(), 0)

