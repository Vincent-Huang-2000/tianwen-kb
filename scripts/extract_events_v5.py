CN_NUM = str.maketrans('一二三四五六七八九', '123456789')
def cn2int(s):
    s = s.strip()
    if s.isdigit(): return int(s)
    if '十' in s:
        parts = s.split('十')
        a = int(parts[0].translate(CN_NUM)) if parts[0] else 1
        b = int(parts[1].translate(CN_NUM)) if len(parts) > 1 and parts[1] else 0
        return a * 10 + b
    if s == '元': return 1
    return int(s.translate(CN_NUM))

