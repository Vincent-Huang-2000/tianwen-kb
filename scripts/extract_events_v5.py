EVENT_KW = {'eclipse_solar':['riyou'],'eclipse_lunar':['yueyou'],
    'comet':['huixing','xingbei','beixing'], 'meteor':['liuxing','yunxing','xingzhui'],
    'guest_star':['kexing']}

all_events = []

for fn in sorted(os.listdir('corpus')):
    if not fn.endswith('.txt') or '_tagged' in fn: continue
    prefix = fn.split('_')[0]
    if prefix not in BOOK_INFO: continue
    dynasty = BOOK_DYNASTY[prefix]
    book = BOOK_INFO[prefix]
    
    with open(os.path.join('corpus', fn), 'r', encoding='utf-8') as f:
        text = f.read()
    clean = re.sub(r'\[[^\]]+\]', '', text)
    
    current_reign = None
    
    for sent in re.split(r'[。；\n]', clean):
        sent = sent.strip()
        if len(sent) < 8: continue
        
        # Track reign context
        for rn in REIGNS:
            if rn in sent and re.search(re.escape(rn)+r'[元一二三四五六七八九十百千\d]+年', sent):
                if DYNASTY_STARTS.get((dynasty, rn)):
                    current_reign = rn
                break
        
        # Extract year
        yr = None
        
        # With reign: REIGN YEAR年
        for rn in REIGNS:
            m = re.search(re.escape(rn)+r'(元|[一二三四五六七八九十百千\d]+)年', sent)
            if m:
                yr_str = m.group(1)
                yr = 1 if yr_str == '元' else cn2int(yr_str)
                ce_base = DYNASTY_STARTS.get((dynasty, rn))
                if ce_base: current_reign = rn
                break
        
        # Bare year (inherit context)
        if yr is None and current_reign:
            m = re.search(r'^(?:.{0,5}?)([一二三四五六七八九十百千\d]+)年', sent)
            if m:
                yr_str = m.group(1)
                try: yr = cn2int(yr_str)
                except: pass
        
        if yr is None or current_reign is None: continue
        ce_base = DYNASTY_STARTS.get((dynasty, current_reign))
        if ce_base is None: continue
        
        # Find event
        evt_type = None
        if '日有食之' in sent or '日蚀' in sent: evt_type = 'eclipse_solar'
        elif '月有食之' in sent or '月蚀' in sent: evt_type = 'eclipse_lunar'
        elif '彗星' in sent or '星孛' in sent: evt_type = 'comet'
        elif '流星' in sent or '陨星' in sent or '星坠' in sent: evt_type = 'meteor'
        elif '客星' in sent: evt_type = 'guest_star'
        else: continue
        
        ce_year = ce_base + yr - 1
        
        event = {'year':ce_year,'type':evt_type,'reign':current_reign,'reign_year':yr,
                 'description':sent.strip()[:200],'source':book}
        all_events.append(event)

# Deduplicate    
seen = set()
unique = []
for ev in all_events:
    key = (ev['year'],ev['type'],ev['description'][:60])
    if key not in seen:
        seen.add(key)
        unique.append(ev)
unique.sort(key=lambda x:x['year'])

stats = {}
for ev in unique: stats[ev['type']] = stats.get(ev['type'],0)+1
print(f'Total:{len(all_events)} Unique:{len(unique)}')
if unique: print(f'Range: {unique[0]["year"]} to {unique[-1]["year"]}')
for t,c in sorted(stats.items(),key=lambda x:-x[1]): print(f'  {t}: {c}')

os.makedirs('data',exist_ok=True)
with open('data/events.json','w',encoding='utf-8') as f:
    json.dump(unique,f,ensure_ascii=False,indent=2)
print(f'Saved {len(unique)} events')
